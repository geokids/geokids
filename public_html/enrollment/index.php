<h2>Enrollment</h2>

<p>Enrollment is scheduled on an annual basis and placement is dependent on an open classroom spot. However, periodically spaces open up throughout the year. Once enrollment for continuing children and families is finalized in the late winter of the preceding school year, new spots are identified. New families will be notified of openings in the early spring.</p>

<p> Interested families must submit a completed waitlist application form accompanied by the non-refundable waitlist fee ($50). </p>

<h2>Waitlist</h2>
Due to the high demand for quality childcare, GeoKids does have a waitlist. As a precaution, we not only strongly recommend that you submit an application(s) as early as possible, but we also urge you to apply to more than one childcare center. Applications are accepted during the mother-to-be's pregnancy, or any time thereafter.</p>

<p> The waitlist is updated in the late winter through a mailing of the Update Waitlist form. The Update Waitlist form must be returned to GeoKids to remain on the waitlist. No additional fee will be required.</p>

<p>Please see the <a href="../community/index.php" title="community page">forms section on community page</a> to download the waitlist application.</p>
<br>

<h2>Admission Process</h2>
<p>Once a family has been notified and accepts an opening, a deposit of one month's tuition is required. The deposit is non-refundable unless a 60-day notice is given prior to the scheduled enrollment date. If an offer of space comes less than 30 days prior to the schedule start, the deposit is non-refundable.</p>

