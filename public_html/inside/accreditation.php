<h2>NAEYC Accreditation</h2>
<p>You have chosen an early childhood program for your child that is accredited by the National Association for the Education of Young Children (NAEYC). NAEYC administers the largest and most widely recognized national, voluntary professionally sponsored accreditation system for all types of early childhood schools and child care centers. NAEYC is the nation's largest organization of early childhood educators.<br>
  <br>

  <Strong> What is Accreditation?</Strong><br>
Early childhood programs accredited by the National Academy of Early Childhood Programs (NAEYC's accreditation department) have voluntarily undergone a comprehensive process of internal self-study, invited external professional review to verify compliance with the Criteria for High-Quality Early Childhood Programs, and been found to be in substantial compliance with the criteria. A copy of these criteria can be obtained from the National Academy of Early Childhood Programs.<br>
<br>
<Strong>What is a high-quality early childhood program?</Strong><br>
A high-quality early childhood program provides a safe and nurturing environment while promoting the physical, social, emotional, and intellectual development of young children. In accredited programs, you will see:</p>
<ul>
  <li>frequent, positive, warm interactions among adults and children</li>
  <li>planned learning activities appropriate to children's age and development, such as block building, art, library time, dramatic play, and active outdoor play</li>
  <li>specially trained teachers</li>
  <li>enough adults to respond to individual children</li>
  <li>many varied age appropriate materials</li>
  <li>a healthy and safe environment for children</li>
  <li>nutritious meals and/or snacks</li>
  <li>regular communication with parents, who are welcomed visitors at all times</li>
  <li>effective administration</li>
  <li>ongoing, systematic evaluation<br>
    <br>
  </li>
</ul>
