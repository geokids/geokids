<h2>Parent Involvement and Education</h2>

<p>Parents are considered an essential component of the program as part of the advisory councils and classrooms.  They provide support with classroom activities, celebrations and school events.  They are a competent and active part of the community of learners that includes children, teachers and other adults.

<h2>Co-operative</h2>

<p>GeoKids is a co-operative program where parents, through co-oping, make up one-third of the classroom staff.  GeoKids emphasizes the mentoring and training of parents to better work with the children.  The school offers many lectures and workshops, including conflict resolution, problem solving, limit-setting, children's perspectives on the world, first aid and CPR, and evaluation of preschool and kindergarten programs.   By working in the classrooms, parents become familiar with the range of children's personalities, interests, and development.   

<h2>What does it mean to co-op in a child's room?</h2>

<p>Co-oping involves a range of tasks depending upon the time of day that you are in your child's classroom.  Parents may do laundry, make snack, help with meal times, read books, or be available for the children to play or just listen to them describe their day.  

<h2>Volunteer Opportunities</h2>

<p>Beyond co-oping, parents have the opportunity to become very involved in their children's education and GeoKids through the following mechanisms: 

<p>Volunteer on Parent run committees including the Board of Directors, the Community Building Committee (CBC), the Executive Director's Resource Committee (DRC), the Gala Committee, and the Workday Committee.

<h2>Education</h2>

<p>Throughout the year, GeoKids hosts many recognized speakers in the field of childhood education.  The Executive Director hosts workshops for parents throughout the year on topics including: 
<ul>
<li>Conflict Resolution</li>
<li>Diversity</li>
<li>Kindergarten Readiness</li>

<li>Sibling Rivalry</li>
<li>Talking with Children about Tough Issues</li>
</ul>

