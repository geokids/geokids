<strong>GeoKids is a community of learners</strong> who think reflectively and collaborate to<br>
ensure that...
<ul><li>children are seen as capable and competent</li>
<li>curiosity and exploration are encouraged</li>
<li>a variety of materials are offered to children that reflect their interests</li>
<li>the pace of the child is respected and honored</li>
<li>reflection and thoughtful intentions are promoted by staff and families</li>
<li>parents are not made passive and are encouraged to be engaged and active in their children's learning.</li></ul>

<strong>Curriculum</strong><br>
Curriculum development is based on the interests of the children, which can be inspired by the materials in their environment. Children are allowed to explore ideas until they are and become masters<br><br>

<strong>Projects</strong><br>
Short and long-term projects are in-depth studies of concepts, ideas, and interests, which arise within the group.  Project ideas originate as the children and teachers construct knowledge together.  Projects can last a few days to several months.<br><br>

<strong>Collaboration</strong><br>
Collaboration is valued and encouraged in small and large groups of children.<br><br>

<strong>Nature</strong><br>
Children are encouraged and are free to explore the outdoors as an extension of the classroom.<br><br>


