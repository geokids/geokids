<h2>Infant/Young Toddlers Program (3 to 24 months old)</h2>

<p>Goals:
<ul>
<li>Foster secure and trusting relationships that support the infant sense of safety and wonder in the world outside the home.</li>
<li>Develop strong 1:1 relationships with every child through care giving, play, and interactions.</li>
<li>Allow each child to dictate the pace of his/her own growth and development.</li>

<li>Nurture the very young child's initiative with encouragement and appropriate guidance.</li>
</ul>

<p>This program maintains a ratio of 3 children per adult.

<h2>Toddler Program (18 to 30 months old)</h2>

<p>Goals:
<ul>
<li>Support toddlers emerging sense of self through relationships, activities, and the environment.</li>
<li>Develop the self-help skills that allow toddlers to feel competent and have a greater sense of independence.</li>
<li>Support their emerging language and problem-solving skills.</li>
<li>Help them to develop a greater awareness of their feelings and other children's feelings.</li>

<li>Support their growing exploration of the world around them.</li>
</ul>

<p>This program has a ratio of 4 children per adult.

