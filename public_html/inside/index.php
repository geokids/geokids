<p>
Goals of Preschool Program:
<ul>
<li>Nurture children's friendship building and social skills through adult support in children's play and activity.</li>
<li>Provide an enriched environment, inviting children to become creative players and problem-solvers.</li>
<li>Complement child-initiated play with adult-initiated small-group times and larger group meetings.</li>

</ul>

<p>Goals of Pre-kindergarten Program:
<ul>
<li>Emphasize positive social relationships and group identification.</li>
<li>Provide an environment for children to pursue long-term projects on topics of their interest.</li>
<li>Provide pre-k skill development through play, activities, group times and meetings in ways that are meaningful to the children.</li>
</ul>

<p>Both programs have a ratio of 8 children per adult. 

