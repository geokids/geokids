<strong>Children</strong>
<br>
All children have preparedness, potential, curiosity, and interest in constructing their learning, in engaging in social interaction and in negotiating with everything that their environment brings to them.<br>
<br>
<strong>Parents</strong>
<br>
Parenting is likely the most important vocation. GeoKids believes that confident parents trust in their abilities to know what is best for their children. We prefer that parents be judged not by their mistakes but by their successes. GeoKids parents partner with their children's caregivers to foster respectful and supportive relationships in their children's lives.<br>
<br>
<strong>Teachers</strong>
<br>
Providing care for children in a group setting is a challenging profession that requires significant training and dedication. At GeoKids, teachers have the space and time they need to be reflective about their own processes, the security to take risks in developing their curricula, and the flexibility to address all of a child's needs: emotionally, academically, and otherwise. Parents often benefit from the language, educational tools and various styles GeoKids' teachers employ. Our children develop close bonds with their teachers that ensure long after the day or the school year ends. Teachers have the full support of parents, staff and administration.<br>
<br>
<strong>Community</strong>
<br>
The children, parents and caregivers who make up the GeoKids community recognize their broader community of friends and neighbors and the potential for collaboration. GeoKids is an active participant in the community for education in the processes of parenting and childhood learning. We welcome opportunities to partner with members of our community to advance early childhood education.
