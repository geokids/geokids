<strong>GeoKids History</strong>
<br>
GeoKids was founded in 1987 by a group of parents who worked on the United States Geological Survey Campus (USGS) and the Stanford Research Institute (SRI). Over the past twenty years, GeoKids has grown into an exciting learning environment for children, families, and educators.<br>
<br>
<strong>Inspiration</strong>
<br>
The GeoKids community is inspired by the work of Jean Piaget, Lev Vygotsky, Lillian Katz, Vivien Paley, and Bev Boss. GeoKids is also greatly influenced by the educators of Reggio Emilia. The GeoKids staff has been studying this philosophy for the past 12 years and has committed to sending two teachers annually to Italy with the study groups organized by Reggio Children.<br>
<br>
<strong>Guiding Principles</strong>
<br>
GeoKids guiding principles are greatly influenced by the Reggio philosophy. Several key principles include:<br><br>
