<h2>GeoKids Vision and Mission</h2

<p>GeoKids is a close and unique community of children, parents and staff. Our first priority is our children, whom we want to be well cared for, stimulated and encouraged to discover the world around them. We also strive to meet the needs of our parents, staff and the greater community. Our vision is that everyone who comes in contact with GeoKids share a common understanding of the program and have respect and appreciation for the program.</p>

<p>GeoKids provides cooperative childcare for families in the general community. We seek to ensure that all voices of children, parents and staff are heard and respected as participants in the GeoKids Community. Children are capable, caring, creative individuals, and GeoKids teaches a philosophy that respects the process of childhood. We believe that parental involvement in the classroom supports young children and helps parents grow along with their children. GeoKids fosters an environment where all caregivers can learn from and mentor each other, always striving to help children achieve their full potential.</p>

