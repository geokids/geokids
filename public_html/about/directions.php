<B>From Bayshore Freeway (US 101)</B><br>
TAKE the Willow Road exit west to Menlo Park.<br>
DRIVE 1 mile to Middlefield Road<br>
TURN RIGHT (northwest)<br>
DRIVE 2 blocks<br>
TURN LEFT on Survey Lane at 345 Middlefield Road.<br>
GeoKids is at the corner on your first left off of Survey Lane.<BR><BR>

<B>From El Camino Real (US 82)</B><br>
TURN NORTHEAST on Ravenswood Road in Menlo Park<br>
DRIVE to the T-junction at Middlefield Road<br>
TURN RIGHT and proceed 2 blocks to the USGS main entrance at 345 Middlefield Road<br>
TURN RIGHT (southwest) on Survey Lane at 345 Middlefield Road.<br>
GeoKids is at the corner on your first left off of Survey Lane.<BR>
<BR>

<B>To Use Public Transportation on the Peninsula</B><br>
TAKE CalTrain to the Menlo Park Station, and then the SamTrans Bus 296 (Purdue and Fordham) to the campus.<br>
Or, to walk to the campus (less than a mile), head south from the train station about 100 yards to Ravenswood Avenue.<br>
TURN LEFT (northeast) and walk down Ravenswood about a half a mile to Middlefield Road<br>
TURN RIGHT (southeast) and the campus is on the right hand side of the street<br>
TURN RIGHT (west) on Survey Lane.<br>
GeoKids is at the corner on your first left off of Survey Lane.<BR><BR>


